
import 'package:flutter/material.dart';

class AppTheme {

  static final primaryColor = Color(0xff097ec2);
  static final secondaryColor = Color(0xffee3235);
  static final accentColor = Color(0xfff5ec3c);

  static ThemeData create() => ThemeData(
    primaryColor: primaryColor,
    accentColor: secondaryColor
  );



}